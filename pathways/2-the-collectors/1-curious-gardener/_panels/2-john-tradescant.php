        <div class="inner">
            <p>Connections with New World explorer John Smith and King Charles I (whose garden Tradescant also tended) led to the donation of other unique artefacts to Tradescant’s collection. These included the robe of ‘the King of Virginia’ and Henry VIII’s stirrups and hawking gear.</p>
