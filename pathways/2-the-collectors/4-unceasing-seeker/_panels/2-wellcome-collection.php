            <div class="text-panel" data-chapter="4">
                <header>
                    <h1>4.	A never-ending quest</h1>
                </header>
                <p>Wellcome’s planned book never appeared. No sooner did he approve an acquisition, than he became distracted by the promise of something more interesting. Boxes and crates piled up in his archives and the preparation of the manuscript was endlessly postponed.</p>
                <p>As the collection expanded to include armour, weapons, medical equipment, amulets and even entire shops, Wellcome’s insatiable appetite for acquiring objects outweighed any inclination to sort, catalogue or even use the books regularly arriving in his stores. The focus of the Wellcome Historical Medical Museum, which was used by researchers, missionaries and colonial travellers, was squarely on collecting – it was never opened to the general public (or ‘stragglers’, as Wellcome once labelled them) in Wellcome’s lifetime.</p>
