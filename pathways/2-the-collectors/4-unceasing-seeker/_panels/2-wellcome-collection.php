            <div class="text-panel" data-chapter="3">
                <header>
                    <h1>3.	A network of collectors</h1>
                </header>
                <p>With an unceasing zeal for collecting, Wellcome bought objects and publications from bazaars, bookshops, pharmacies, dealers and private collectors. He employed a network of agents to purchase items under assumed names and engaged researchers to sift through the information they acquired. Stockpiling ideas as much as objects, Wellcome forbade his agents from making personal purchases and refused his researchers’ requests to investigate areas he intended to study himself.</p>
