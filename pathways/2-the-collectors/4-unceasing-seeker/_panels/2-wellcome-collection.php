            <div class="text-panel" data-chapter="5">
                <header>
                    <h1>5.	Ultimate recognition</h1>
                </header>
                <p>Wellcome famously described his plans as existing in his mind, ‘like a jigsaw puzzle, and gradually I shall be able to piece it together’. Although he never completed the research he likened to a ‘struggle for light’, Wellcome’s vision drove the eclectic growth of his collection for many years. Today, Wellcome Collection and the Wellcome Library still collect and present a broad interpretation of medical history.</p>
                <p>Ultimately, Wellcome obtained the recognition he so desperately craved: at the age of 79 he was made both a Knight of the realm and a Fellow of the Royal Society.</p>
