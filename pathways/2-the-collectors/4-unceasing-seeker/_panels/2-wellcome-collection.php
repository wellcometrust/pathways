                <header>
                    <h1>2. An ambitious aim</h1>
                </header>
                <p>Collecting offered Wellcome, a businessman from the American mid-West, a path towards scholarly respect and recognition within London society. In the 1890s he conceived a plan to research and publish an encyclopaedic book, a chronicle of human ingenuity that would document the use of animal products in medicine from antiquity to contemporary times. His ambitious aim would be achieved by collecting books and artefacts and empirically testing the chemistry of various substances.</p>
