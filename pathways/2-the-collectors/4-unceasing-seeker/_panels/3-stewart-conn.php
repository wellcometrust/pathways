            <p>As though a neolithic arrowhead he’d unearthed<br>
            at the age of four had entered his bloodstream,<br>
            its sliver of flint sparking an obsession, the items<br>
            he acquired over the years ranged from Darwin’s<br>
            whalebone walking-stick, Napoleon’s toothbrush<br>
            and a pair of Florence Nightingale’s mocassins<br>
            to shrunken heads and tons of ancient armour.</p>

            <p>But despite all his squirrelling, the museum<br>
            to house them remained illusory. Picture him,<br>
            his explorer’s garb and trappings laid aside,<br>
            increasingly hemmed in, until overwhelmed<br>
            by the mouldering mountain, moth-eaten,<br>
            worm-ridden, filling his Willesden warehouse<br>
            to overflowing, he ended up part haunted,</p>

            <p>part devoured by more than enough to fill five<br>
            Louvres but not some insatiable chasm within.<br>
            Garlanded and afforded the scholastic kudos<br>
            he craved, a vast fortune securely amassed,<br>
            did he at the last find respite from cupidity<br>
            or in a lonely hall of mirrors cower from those<br>
            behemoths hemming him in, their foetid breath?</p>
