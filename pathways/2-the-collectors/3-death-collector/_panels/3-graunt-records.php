            <p>Graunt collected a few ‘scattered bills’ and began tabulating the data they shared. Like a merchant’s account book, Graunt’s tables provide a continuous record of additions and subtractions to the City’s population.</p>
            <p>Graunt also interpreted the information he collected, documenting insights about the causes and trends behind the changing market for trade.</p>
            <ul>
            <li>In the great plague years, 1 in 5 Londoners died, and another two-fifths fled to the country.</li>
            <li>After a plague outbreak, it takes two years to repopulate the City.</li>
            <li>The population of London doubles four times faster than the population in the country.</li>
            <li>There are about 24,000 teeming women in the parishes in and around London.</li>
            <li>There are about 81,000 fighting men in and about London.</li>
            <li>One-fifth of all Londoners live in Westminster, Lambeth, Islington, Hackney, Redriff, Stepney and Newington.</li>
            <li>1 in 2,000 Londoners are murdered.</li>
            <li>1 in 100 women die in childbirth.</li>
            <li>36 per cent of children die before the age of six.</li>
            <li>Autumn is the unhealthiest season.</li>
            <li>Deaths due to rickets, stopping of the stomach, rising of the lights and scurvy are all increasing.</li>
