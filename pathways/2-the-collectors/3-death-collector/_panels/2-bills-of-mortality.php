            <p>In a city still reeling from the massive loss of life caused by the great Plague, each time a Londoner died a bell tolled to summon ‘the Searchers’, matronly (and often drunken) women who viewed the corpse and made any other necessary enquiries in order to establish the cause of death.</p>
            <p>Within days this information appeared in the ‘Bills of Mortality’, published each week and sold on subscription for 4 shillings a year. Until they peaked Graunt’s interest, the Bills weren’t used for much more than feeding ghoulish gossip.</p>
