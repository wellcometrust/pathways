            <h1>How could these powers be explained?</h1>
        </header>

        <p>The Scottish surgeon James Braid developed a radical new theory. He investigated the trance states of Indian yogis and fakirs, particularly their state of mental “hibernation”, in which some could be buried alive for days.</p>
        <p>Experimenting on his friends, Braid discovered that he could induce deep trances using simple mental techniques: suggestion, controlled breathing and focused attention.</p>
        <p>Braid announced that there was no need to believe in animal magnetism, occult powers or vital energies. Trance states, and all the mysterious phenomena they generated, could be produced by the mind alone.</p>
        <p>He coined a new term for the process: hypnotism.</p>
